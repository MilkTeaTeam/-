    private void txtStuIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtStuIdKeyPressed
        if(evt.getKeyCode()==10){//当按下回车键
            //获取输入的学号
             String  stuId=txtStuId.getText();
             String stuName=DBCon.queryName("Student", stuId);
             if(stuName!=null){
                 lblStuName.setText(stuName);
             }
        }
    }//GEN-LAST:event_txtStuIdKeyPressed

