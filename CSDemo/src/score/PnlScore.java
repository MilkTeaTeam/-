    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        //获得学号、课程编号、成绩和学期
        String stuId=txtStuId.getText();
        String courseId=txtCourseId.getText();
        String score=txtGrade.getText();
        String term=txtTerm.getText();
        
        String sql="insert into Score (stuId,courseId,grade,term) values('"+stuId+"','"+courseId 
                +"',"+score+",'"+term+"')";
        System.out.println(sql);
        if(DBCon.updateData(sql)){
            //刷新数据
            String sql2="Select s.stuId,stuName,c.courseId,courseName,grade,term from Student s,Score g,Course c"
                + " where s.stuId=g.stuId and g.courseId=c.courseId"; 
            data=DBCon.queryData(sql2);
            dtm.setDataVector(data, title);
            JOptionPane.showMessageDialog(null, "录入成绩数据成功","系统信息",JOptionPane.INFORMATION_MESSAGE);
           
        }else{
            JOptionPane.showMessageDialog(null, "录入成绩数据失败","系统信息",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed


