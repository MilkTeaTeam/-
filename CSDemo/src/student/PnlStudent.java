    //在文本框中显示数据
    public void showTextData(int index){
        //获取该学生的信息
        Vector stuV=data.get(index);
        //在文本框中显示数据
        txtStuId.setText((String)stuV.get(0));
        txtStuName.setText((String)stuV.get(1));
        String sex=(String)stuV.get(2);
        if(sex.equals("男")){
            radMan.setSelected(true); //男单选按钮选中
        }else{
            radWoman.setSelected(true); //女单选按钮选中
        }
        Object birth=stuV.get(3);
        txtBirth.setText(birth.toString());
        lockText();
    }
    
