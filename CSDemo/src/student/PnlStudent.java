    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        index++;
        if(index>=data.size()){
            index=data.size()-1;
            JOptionPane.showMessageDialog(null, "已经是最后一条记录了","系统信息",JOptionPane.INFORMATION_MESSAGE);
        }
        showTextData(index);
        //表格中高亮度显示数据同步
        tblStu.setRowSelectionInterval(index, index);
    }//GEN-LAST:event_btnNextActionPerformed

    private void btnPreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPreActionPerformed
        index--;
        if(index<0){
            index=0;
            JOptionPane.showMessageDialog(null, "已经是第一条记录了","系统信息",JOptionPane.INFORMATION_MESSAGE);
        }
        showTextData(index);
        //表格中高亮度显示数据同步
        tblStu.setRowSelectionInterval(index, index);
    }//GEN-LAST:event_btnPreActionPerformed

    //锁定文本框的方法
    void lockText(){
        txtStuId.setEnabled(false);
        txtStuName.setEnabled(false);
        txtBirth.setEnabled(false);
        radMan.setEnabled(false);
        radWoman.setEnabled(false);
        btnNext.setEnabled(true);
        btnPre.setEnabled(true);
        btnAdd.setEnabled(true);
        btnSave.setEnabled(false);
        btnCancel.setEnabled(false);
    }
    //锁定文本框的方法
    void unlockText(){
        txtStuId.setEnabled(true);
        txtStuName.setEnabled(true);
        txtBirth.setEnabled(true);
        radMan.setEnabled(true);
        radWoman.setEnabled(true);
        btnNext.setEnabled(false);
        btnPre.setEnabled(false);
        btnAdd.setEnabled(false);
        btnSave.setEnabled(true);
        btnCancel.setEnabled(true);
    }
