    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //获取用户输入的数据
        String stuId=txtStuId.getText();
        String stuName=txtStuName.getText();
        String birth=txtBirth.getText();
        String sex="";
        
        if(radMan.isSelected()){
            sex="男";
        }else if(radWoman.isSelected()){
            sex="女";
        }
        
        //验证输入必填项是否填了
        if(stuId.length()>0 && stuName.length()>0 && sex.length()>0 && birth.length()>0){
            String sql="insert into Student values('"+stuId+"','"+stuName+"','"
                         +sex+"','"+birth+"')";
            //调用DBCon的插入数据的方法来插入数据
            if(DBCon.updateData(sql)){
                //刷新数据
                data=DBCon.queryData("select * from Student");
                showTableData();
                //锁定文本框
                lockText();
                
                JOptionPane.showMessageDialog(null, "添加数据成功","系统信息",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "添加数据失败","系统信息",JOptionPane.ERROR_MESSAGE);
            }
            
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

