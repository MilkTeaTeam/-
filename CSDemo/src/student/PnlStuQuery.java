    private void btnQueryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQueryActionPerformed
        //获取输入的查询条件
        String stuId=txtStuID.getText();
        String stuName=txtStuName.getText();
        String sex="";
        if(radMan.isSelected()){
            sex="男";
        }else if(radWoman.isSelected()){
            sex="女";
        }
        String birth1=txtBirth.getText();
        String birth2=txtBirth2.getText();
        
        String sql="Select * from Student where 1=1";
        
        String query="";
        if(stuId.length()>0){
            query="stuId like '%"+stuId+"%'";
        }
        
        if(stuName.length()>0){
            if(query.length()>0){
                query=query+ " and stuName like '%"+stuName+"%'";
            }else{
                query="stuName like '%"+stuName+"%'";
            }
        }
        
        if(sex.length()>0){
            if(query.length()>0){
                query=query+ " and sex='"+sex+"'";
            }else{
                query="sex='"+sex+"'";
            }
        }
        
        String birthQuery="";
        if(birth1.length()>0 && birth2.length()==0){
            birthQuery="birth>='"+birth1+"'";
        }else if(birth1.length()==0 && birth2.length()>0){
            birthQuery="birth<='"+birth2+"'";
        }else if(birth1.length()>0 && birth2.length()>0){
            birthQuery="birth>='"+birth1+"' and birth<='"+birth2+"'";
        }
        
        if(birthQuery.length()>0){
            query=query+" and "+birthQuery;
        }
        
        if(query.length()>0){
            sql=sql+" and "+query;
        }
        System.out.println(sql);
        data=DBCon.queryData(sql);
        showTableData();
    }//GEN-LAST:event_btnQueryActionPerformed

