    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        //打开学生详细信息面板
        //--3 清空面板父类容器(内容面板)的所有组件
        JPanel pnl=(JPanel)this.getParent();
        pnl.removeAll();
        //--4 加载PnlStuDetail面板（学生详细信息面板）
        PnlStuDetail pnlStuDetail=new PnlStuDetail(stuId);
        //--5 设置当前内容面板的布局为边框式布局
        pnl.setLayout(new BorderLayout());
        //--6 加载学生详细信息的面板
        pnl.add(pnlStuDetail,BorderLayout.CENTER);
        //--7 刷新窗口
        pnl.getParent().validate();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        //确认删除
        if(JOptionPane.showConfirmDialog(null, "你确认要删除该数据吗？","确认删除",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE)==JOptionPane.YES_OPTION){
            String sql="delete from Student where stuId='"+stuId+"'";
            if(DBCon.updateData(sql)){
                //刷新数据
                data=DBCon.queryData("Select * from Student");
                dtm.setDataVector(data, title);
                
                JOptionPane.showMessageDialog(null, "删除数据成功","系统提示",JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(null, "删除数据失败","系统提示",JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


